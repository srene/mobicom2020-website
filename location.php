<b>Visit El Arco ("The Arch") of Cabo San Lucas</b>

<p>El Arco is a three-story rock formation formed 115 million years ago from natural erosion during the Cretaceous period. El Arco is located at the southernmost point of the Baja California Peninsula, where the Pacific Ocean becomes the Gulf of California. The rock formation is a popular venue for tourists and sea lions.</p>

