<h5><b>History and Culture of Los Cabos</b></h5>

<p>Approximately 14,000 years ago, nomadic tribes arrived to what is now the peninsula of Baja California. The three largest populations in the peninsula, prior to the arrival of the Spaniards, were the Pericu, Guaicura, and Cochimi. The Pericu inhabited the southern tip of the peninsula, including current day Cabo San Lucas which as then named Yenecamu (“the place where two waters meet”). </p>

<p>The founding of Cabo San Lucas is generally credited to a time in the late 1700’s when Cipriano Ceseña, his wife Luciana, and their 13 children first settled near El Medano beach. Since then, Cabo has grown and become one of Mexico’s top tourist destinations. Fun fact: part of the early history of Cabo is known thanks to the a book by the japanese authors Maekawa Bunzo and Sakai Junzo</p>

<p>In the mid 1970’s Mexico’s government begun developing the southern tip of Baja California into a tourist destination. Since then, Cabo’s population has grown steadily along with the infrastructure, tourist attractions, hotels, and has helped the state’s economy diversify away from salt mining and agriculture industries, into tourism and eco-tourism.</p>

