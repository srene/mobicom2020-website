<h2 class="text-center">Camera-Ready Instructions</h2>

<p><b>Note that conference proceedings will be published in 2 batches before the conference start date, being the target date for publication of the first batch January 2020.</b></p>

<p>Camera-ready papers for the Mobicom 2020 proceedings are due <b>February 10th</b>. In essence, you need to do the following:</p>
<ul>
  <li>Work with the shepherd to address all the reviewers’ comments. The shepherd will be ultimately responsible for accepting or rejecting your submission.</li>
  <li>Fill out copyright form, information on how to do this will be emailed to authors automatically in a separate mail.</li>
  <li>Adapt your stylesheet to the most recent ACM template, include bibliographic and copyright strip.</li>
  <li>Authors should follow the guide to the MobiCom 2020 submission preparation instructions available <a href="cfp.php#instructions">here</a>.
</ul>


<h4>Complete rights management form</h4>
<ul>
  <li>Before submitting camera-ready papers, you need to complete the rights management form. You will be notified by email once these forms are available and ready to sign.</li>
  <li>As rights management emails are sent from an automated system, there is a chance that emails sent will wind up in SPAM folders. Please make sure that you and your authors set email SPAM settings to allow emails from “rightsreview@acm.org”.</li>
</ul>
<h4>Format paper</h4>
<ul> <li>Papers should be no more than 12 pages single-spaced pages for text and figures + 4 pages for references. <b>Page limit is also 12 pages for camera-ready challenge, experience and verification papers.</b></li>
  <li>Camera-ready papers should adhere to the ACM templates that can be found here: <a href="http://www.acm.org/publications/proceedings-template">http://www.acm.org/publications/proceedings-template</a>. Templates are provided for formatting papers in MS Word and LaTeX. In LaTeX, you should use <code>\documentclass[sigconf]{acmart}.</code></li>
  <li>Note that the originally submission instructions suggested using the old ACM template. The font size in the submission template was 10pt, the new template will use 9pt. Using the new ACM template means that you have more space, to address the comments by the reviewers. Please also double check to make sure that you are using the new template (linked in the previous bullet).</li>
  <li>Please do NOT number the pages of your camera-ready paper (by default page numbers are disabled in the ACM template).</li>
  <li>You should provide proper indexing information in the final version according to the ACM Computing Classification System (CCS). More information about the ACM CCS is available on the ACM CCS website.</li>
  <li>After completing their rights management form, you will be emailed the correct rights management text and bibliographic strip to place within your paper. It is essential that you add the text into your paper.</li>
  <li>Avoid using any special characters or non-standard fonts. We must be able to display and print your submission exactly as we receive it using only standard tools and printers, so we strongly suggest that you use only standard fonts embedded in the PDF file.</li>
</ul>
<h4>Submit camera-ready version</h4>
<ul>
  <li>Go to the Mobicom 2020 submission page and edit your paper: <a href="https://mobicom20.hotcrp.com/">https://mobicom20.hotcrp.com/</a>.</li>
  <li>Submit the source files and PDF of your camera-ready paper.</li>
</ul>
<h4>Questions</h4>
<p>If you have any questions about the camera-ready process, feel free to contact Mobicom 2020 publication co-chairs Onur Ascigil  (<a href="mailto:o.ascigil@ucl.ac.uk">o.ascigil@ucl.ac.uk</a>) or Jagmohan Chauhan (<a href="mailto:jc2161@cam.ac.uk">jc2161@cam.ac.uk</a>).</p>

