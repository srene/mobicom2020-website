<h5><b>Paper Submission Instructions</b></h5>
All paper submissions will be handled electronically at the <a href="https://mobicom20.hotcrp.com/">MobiCom’20 paper submission website</a>. Authors should prepare a PDF version of their full paper.
<br/><br/>
Before final submission, you are expected to make sure that your paper complies with the following requirements. You are strongly discouraged from violating these requirements with the aim of including additional material: submissions that violate the formatting requirements may not be reviewed. You can visually inspect a page-by-page report of your paper format using the same tool as the submission system via a separate <a href="https://www.sysnet.ucsd.edu/sigops/banal/index.php">online form</a>. After the submission deadline, we will use the same tool to check the conformance of papers. The format checking tool uses heuristics and can make mistakes. The PC chairs will manually inspect and possibly reject those papers with evident format violations. However, no paper will be rejected due to format violations without first being checked by hand.
<br/><br/>
Any papers that do not adhere to the following guidelines may be immediately rejected:
<br/><br/>

<ul style="text-align:left">
<li>Full papers: Contain no more than twelve (12) single-spaced and numbered pages, including figures, tables, any appendices, and any other material, followed by as many pages as necessary for bibliographic references. Papers whose non-bibliographic content is longer than 12 pages will not be reviewed.</li>
<li>Experience/Verification/Challenge Papers: Contain no more than eight (8) single-spaced and numbered pages, including figures, tables, any appendices, and any other material, followed by as many pages as necessary for bibliographic references. Papers whose non-bibliographic content is longer than eight pages will not be reviewed.</li>
<li>Font size no smaller than 10 points.</li>
<li>Double column format with each column having dimensions 9.25 inches x 3.33 inches, a space of 0.33 inches between the two columns, and with no more than 55 lines of text per column.</li>
<li>Submissions must be in PDF (Portable Document Format) authors should ensure that they are compatible with Adobe Acrobat (English version). Any other formats, including Postscript and MS-Word, will not be accepted.</li>
<li>Fit properly on US letter-sized paper (8.5 inches by 11 inches).</li>
<li>Authors' names must not appear anywhere in the paper or in the PDF file. The PDF file must also not contain any embedded hyperlinks, as these may compromise reviewer anonymity.</li>
<li>Limit the file size to less than 15 MB: contact the PC chairs if you have a file larger than this limit.</li>
</ul>
<br/><br/>

You may find these <a href="https://www.acm.org/publications/proceedings-template">templates</a> useful in complying with the above requirements, but as an author you bear the final responsibility to verify (manually or through the above online paper checker) that your submission is format-compliant. <br/><br/>

All submitted papers will be evaluated through a double-blind reviewing process, with the identities of the authors withheld from the reviewers. Please direct any questions about the paper submission process to the Program Co-Chairs Bozidar Radunovic and Qian Zhang at mobicom20-pc-chairs __at__ acm.org.
<br/><br/>

<h5><b>Awards</b></h5>
All accepted regular papers will be considered for the <b>Best Paper Awards</b>. We will also have a <b>Best Community Paper Award</b>. Among all accepted papers, the program committee will select one that contributes the most to the broader research community in terms of real data, or new software, hardware or other artifacts.
<br/><br/>
<h5><b>Video Presentation</b></h5>
The authors of each accepted paper will be asked to submit a one-minute video to introduce the research presented in their paper to the general public. These videos will be placed on the conference website up to two weeks before the conference, and will be played during the conference. A selected set will be featured by the social media channels related to the conference.
<br/><br/>
