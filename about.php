<h5><b>About SIGMOBILE and ACM</b></h5>
<p>
The <a href="http://www.sigmobile.org/mobicom/">annual MobiCom conference series</a> is sponsored by <a href="http://www.sigmobile.org/">ACM SIGMOBILE</a>, the Association for Computing Machinery's Special Interest Group on Mobility of Systems, Users, Data, and Computing. Founded in 1947, <a href="http://www.acm.org/">ACM</a> is the world's first educational and scientific computing society. Today, ACM serves a membership of more than 80,000 computing professionals in more than 100 countries in all areas of industry, academia, and government.<br/><br/>

ACM SIGMOBILE addresses a spectrum of topics with one common theme: mobility. The group's technical scope is at the intersection of mobile computing devices and wireless networks, addressing the convergence of mobility, computing, and information organization, its access, services, management, and applications. Members of ACM SIGMOBILE are active in the development of new technologies and techniques for mobile and wireless computing and communications.<br/><br/>

SIGMOBILE has members from around the world, from academic organizations, industry research and development, government, and other interested individuals. Members of SIGMOBILE receive the quarterly journal, <a href="http://www.sigmobile.org/pubs/mc2r/">Mobile Computing and Communications Review (MC2R)</a>, and are also able to receive announcements via the SIGMOBILE-moderated members-only email distribution list, keeping them informed of the latest happenings in our field, including new opportunities to share their research and to meet with friends and colleagues at conferences and other events. SIGMOBILE members also are eligible for the lowest generally available registration fee for any event sponsored or co-sponsored by SIGMOBILE, and for the many events sponsored by other organizations offered in-cooperation with SIGMOBILE.
</p>
<br/>

<h5><b>ACM SIGMOBILE Harassment Policy</b></h5>
<p>
The open exchange of ideas and the freedom of thought and expression are central to the aims and goals of SIGMOBILE; these require an environment that recognizes the inherent worth of every person and group, that fosters dignity, understanding, and mutual respect, and that embraces diversity. The ACM Code of Ethics embraces the "values of equality, tolerance, respect for others, and the principles of equal justice." For these reasons, SIGMOBILE is dedicated to providing a harassment-free conference experience.<br/><br/>

Harassment is unwelcome or hostile behavior, including speech that intimidates, creates discomfort, or interferes with a person's participation or opportunity for participation, in any SIGMOBILE event. Harassment in any form, including but not limited to harassment based on race, gender, religion, age, color, national origin, ancestry, disability, sexual orientation, or gender identity, will not be tolerated. Harassment includes the use of gratuitous language or sexual imagery in public presentations and displays, degrading verbal comments, deliberate intimidation, stalking, harassing photography or recording, inappropriate physical contact, and unwelcome sexual attention.<br/><br/>

Conference participants violating these standards may be sanctioned, expelled from the conference or asked not to attend future conferences or conference events, at the discretion of the conference organizers and the SIGMOBILE executive committee.<br/><br/>

If you believe you have been harassed, you are encouraged to report the incident in confidence to the MobiCom 2019 General Chairs Sharad Agarwal and Ben Greenstein at mobicom19-gc __at__ acm.org.
</p>
