<h2 class="text-center">Call for Papers</h2>

The ACM MobiCom conference seeks papers describing significant research contributions in the field of wireless networking and mobile computing. MobiCom solicits papers that address important research challenges, including but not limited to communication networks, energy, computing, security and user experience. Successful papers should propose novel ideas to attack such problems through rigorous analysis, system design, and/or real-world measurement and deployment of mobile networks, systems and applications.
<br/><br/>

We want MobiCom’20 to be daring and emphasize novelty and creativity. The more novel the concept, the harder it can be to fully develop or evaluate all aspects, and the review process will take this into account. We encourage authors to discuss not only the significance but also the limitations of their ideas. We strongly encourage authors to describe how they will provide access to well-documented datasets, modeling and/or simulation tools, and codebases to support the reproducibility of their methods. MobiCom continues the two-deadline model and the reviewing process with one-shot revision piloted last year to improve the quality and timeliness of publications.
<br/><br/>

We invite submissions on a wide range of mobile computing and wireless networking research, including but not limited to:
<ul style="text-align:left">
<li>Applications of machine learning to mobile/wireless research</li>
<li>Backscatter communication and wireless power</li>
<li>Edge computing</li>
<li>Embedded and energy-harvesting systems</li>
<li>Experimental platforms and infrastructures for wireless/mobile research</li>
<li>Implanted and wearable computing</li>
<li>Intersection between mobile/wireless research, cloud computing, and software-defined networking</li>
<li>Long-range/Low-power wide-area wireless networking</li>
<li>Low-latency networking</li>
<li>Machine-to-machine communications</li>
<li>Millimeter wave and terahertz communications</li>
<li>Mobile data science & analysis</li>
<li>Mobile health</li>
<li>Mobile web, video, virtual reality, and other applications</li>
<li>Next-generation 5G mobile networks</li>
<li>Novel applications of wireless signals</li>
<li>Robotic and drone-based networking</li>
<li>Security and privacy issues/solutions for mobile/wireless systems</li>
<li>Sensing with radio, light, sound, and acoustics</li>
<li>Ubiquitous computing and mobile human computer interaction</li>
<li>Visible light communications</li>
<li>Wireless localization and tracking</li>
</ul>

In addition to full research papers as described above, MobiCom also invites submission in the following three categories:<br/><br/>

<b>Challenge Papers</b> <br/>
The conference invites submissions of short papers (limited to eight pages excluding references) that present revolutionary new ideas or that challenge existing assumptions prevalent among the wireless research community. These "challenge papers" should provide stimulating ideas or visions that may open up exciting avenues and/or influence the direction of future research. Descriptions of new products or evolution of existing work are not appropriate topics for papers in this category. While an exhaustive evaluation of the proposed ideas is not necessary, insight and in-depth understanding of the issues is expected. Challenge papers will be reviewed by the program committee and if selected, become part of the conference technical program and be published in the conference proceedings. Challenge papers should be submitted using the same submission procedure adopted for the full papers. The title of these papers must bear a "Challenge:" prefix.
<br/><br/>
<b>Experience Papers</b> <br/>
The conference invites submission of short papers (limited to eight pages excluding references) that present extensive experiences with implementation, deployment, and operations of mobile systems and wireless networks. Desirable papers are expected to contain real data as well as descriptions of the practical lessons learned. The experience papers will be evaluated by the program committee, primarily for (i) richness of their data or experiences, (ii) inferences drawn or lessons learned, (iii) discovery of new problems, and (iv) their impact/potential impact on current and future mobile systems and wireless networks, as well as on society. Note that a paper that builds a system and presents a limited experimental evaluation is NOT suitable as an experience paper. Selected experience papers will become a part of the conference technical program and will be published in the conference proceedings. Experience papers should be submitted according to the same submission procedure as full papers, and their title must bear an "Experience:" prefix.
<br/><br/>
<b>Verification Papers</b> <br/>
MobiCom welcomes contributions that seek to verify and/or characterize recent breakthrough results in mobile computing using rigorous experimental methodologies with the goal of extensively and thoroughly characterizing the operating parameters under which these results can be reproduced. Such submissions should be short papers (limited to eight pages excluding references) with "Verification:" as a prefix in the title. Selected verification papers will become a part of the conference technical program and will be published in the conference proceedings. Verification papers should be submitted according to the same submission procedure as full papers.
<br/><br/>

<h5><b>Two Deadlines and Reviewing Process with One-shot Revision</b></h5>
MobiCom’20 offers authors the options of two deadlines and adds a one-shot revision process to enhance the timeliness and quality of the scientific results from the research community. Each paper will go through the same rigorous reviewing process as in previous MobiCom conferences. The review decisions will be one of the following:
<br/><br/>

<ul style="text-align:left">
<li>Accept: Accepted papers will be presented at the MobiCom conference and appear as part of the proceedings. In the meantime, authors are permitted to talk about these upcoming publications, e.g., listing them on CVs. The publications will also be put on the MobiCom website.</li>
<li>Reject: Rejected papers cannot be resubmitted until 11 months have passed since the last MobiCom deadline they were submitted to. We strongly encourage the authors to consider the feedback they got before submitting the paper to MobiCom 2020.</li> 
<li>One-Shot-Revision: Papers where a revision is required will be given specific action points and can be resubmitted for the next deadline. For such papers, the reviewers will be required to articulate a maximum of three changes/clarifications that they would like to see. After the resubmission, the same reviewers will evaluate the paper based on whether the authors have properly addressed the reviewers’ requests. Unlike in the shepherding process, the requested action points may include running additional experiments that obtain specific results. During this revision period, the paper is still considered under review to MobiCom and therefore cannot be submitted to other conferences unless the authors first withdraw it from consideration. We expect the one-shot revision to be used only for a small fraction of the papers.</li>
</ul>

