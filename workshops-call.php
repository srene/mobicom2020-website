<h2 class="text-center">Call for Workshops</h2>

<p>ACM MobiCom is the premier international conference dedicated to addressing emerging topics in mobile and wireless networking and computing. MobiCom 2019 will hold multiple full or half day workshops on October 21 (Monday), 2019 and October 25 (Friday), 2019 of the conference week. Workshop papers will be included with the MobiCom proceedings and published in the ACM Digital Library.</p>

<p>We solicit workshop proposals on new and emerging topics that broadly relate to the general areas of mobile communications, wireless networking and mobile computing.</p>

<p>The ideal workshop proposal will focus on a timely and emerging topic and be able to attract a number of interesting and high-quality contributions that spark discussions in an informal setting. The goal of workshop papers is primarily to stimulate interactive conversations and inspire new research ideas. Proposals on emerging or disruptive topics that are likely to generate significant interest in the community will be selected. </p>

<p>Furthermore, given that this is the first MobiCom and SIGMOBILE event to be held in Mexico, we especially solicit workshops on topics with high local & regional relevance (e.g., to Mexico/Central/South America & emerging economies).</p>

<p>MobiCom 2019 will likely feature a mix of some returning and some new workshops. Workshop proposals may be either research-track or industrial-track:</p>

<b>Research-Track Workshops</b><br/>
<p>We encourage more conventional research-oriented workshops that address compelling and growing fields of study. Topics should be grounded and clearly relate to the ACM MobiCom community, and its core areas of interests (e.g., wireless networking and mobile computing). However, we anticipate workshop proposals will also introduce and connect to a set of topics of broader importance, including but not limited to: autonomous cars and systems, healthcare and medical domains, safety/security/privacy, distributed ledgers and cryptocurrencies, machine learning/artificial intelligence, diversity and ethics, technology issues for developing regions, and novel radio/hardware/processor architecture technologies.</p>

<b>Industrial-Track Workshops</b><br/>
<p>In addition to the research-track workshops detailed above, we encourage proposals that are more oriented towards industry. Examples include (but are not limited to) workshops themed around (i) a specific industry vertical or pertaining to specific emerging standards (e.g., automotive, smart homes, and / or healthcare); and, (ii) hands-on experience workshops involving mobile devices, sensor platforms, and / or  wearables.</p>

<h5>Proposal Submission Instructions</h5>

The proposal should be no longer than three pages and should include at least the following:

<ul>
<li>The name of the workshop.</li>

<li>Proposal track (research or industrial) in the title</li>

<li>The name, address, and short bio (up to 200 words) of each organizer (up to three).</li>

<li>A motivation and rationale that includes a brief description of the technical issues to be addressed, and why those issues are timely.</li>

<li>The list of names and affiliations of potential program committee members.</li>

<li>The planned format of the workshop, including a strategy to facilitate lively discussions and attendee involvement.</li>

<li>If previous editions have been held, a description of past versions of the workshop, including the number of submitted and accepted papers, and number of attendees.</li>

<li>A description of the publicity plan.</li>

<li>A call for papers with workshop deadlines (aligned with the relevant Important Dates below).</li>
</ul>

<h5>Submissions</h5>
