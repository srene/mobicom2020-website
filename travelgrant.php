<h2 class="text-center">Student Travel Grant</h2>

<h5><b>Travel Grant Application</b></h5>
<p>Grants are available only for full-time students attending colleges and universities in and outside the US. Women, undergraduates and underrepresented minority students are particularly encouraged to apply.</p>

To apply for a travel grant, the student applicant should send the following material to <a href="mailto:mobicomstg2019@gmail.com">mobicomstg2019@gmail.com</a>:

<ol>
<li>A CV that includes country of origin and country of residence</li>
<li>SIGMOBILE student member number, if the student has one. A SIGMOBILE student membership costs $20 for one year and can be applied for <a href="https://www.sigmobile.org/about/membership.html">here</a>.</li>
<li>An application letter that includes:</li>
<ul>
<li>The titles of any accepted MobiCom work (conference or workshop papers, posters, demonstrations, etc.)</li>
<li>Whether the student is a first-time attendee of MobiCom</li>
<li>If the student is not a first-time attendee of MobiCom, whether the student has received a MobiCom travel grant in the past</li>
<li>A brief summary of the student’s research interests and accomplishments to date</li>
<li>A description of areas reflected in the MobiCom program that would impact the student's research</li>
<li>A statement about why attending the conference is important to the student</li>
<li>An estimate of the following costs of attending MobiCom 2019: airfare, hotel, and conference registration. It is anticipated that travel grants will partially cover these attendance costs.</li>
</ul>
</ol>

A recommendation letter from the advisor of the student applicant is also required. The letter should provide the following:
<ol>
<li>A confirmation that the student is a bona fide Ph.D. candidate or a M.Sc./B.Sc. student in good academic standing at the given institution.</li>
<li>The impact of the MobiCom program material on the student’s research progress.</li>
<li>Other benefits the student would derive from attending the conference.</li>
<li>A description of the student applicant's strengths and potential contributions to the mobile computing and networking field.</li>
<li>A statement of financial commitment to pay the remainder of the student applicant’s travel cost not covered by the grant.</li>
</ol>

The advisor's letter should be sent to <a href="mailto:mobicomstg2019@gmail.com">mobicomstg2019@gmail.com</a> directly by the advisor.<br/><br/>

<h5><b>Selection Criteria</b></h5>
<p>Selection will be based on student merit qualifications (e.g., paper authorship) and financial need (e.g., travel distance/cost from home campus, availability of grant funds, etc). Priority will be given to women, undergraduates, underrepresented minorities, and first-time attendees.</p>

