<h5><b>The ACM Student Research Competition (SRC)</b></h5>
ACM MobiCom 2020 will host a <b><em>virtual</em></b> <a href="https://src.acm.org/">ACM Student Research Competition (SRC)</a> alongside the Posters and Demos Program. SRC offers graduate and undergraduate students an opportunity to present their original research, and rewards outstanding student research. It aims to give students an opportunity to meet with conference attendees and distinguished researchers to receive feedback on their research. Although this year Mobicom will be held virtually, we are committed to ensure students the opportunity to (virtually) interact with conference attendees via chat channels during the entire conference and also during the virtual poster/demo sessions.
<br/><br/>
<h5><b>Guidelines</b></h5>
<ul>
<li>SRC has two categories - <b><em>Graduate Research</em></b> and <b><em>Undergraduate Research</em></b>:
<ul>
<li><b><em>Graduate Research</em></b>: Entries in the graduate category are open to Masters or PhD candidates. Only individual research contributions are eligible for SRC. Multi-author submissions are allowed but students should be prepared to present (and will be evaluated on) their own contribution to the project.</li>
<li><b><em>Undergraduate Research</em></b>: Team projects are accepted from undergraduate students. The team must designate one of the undergraduate authors for presentation at the conference.</li>
</ul>
<li>Participants must be currently enrolled in a university.</li>
<li>Valid ACM student membership is required for the designated student presenter.</li>
</ul>

<h5><b>Selection Process</b></h5>
The first round of selection is held with the MobiCom poster/demo sessions, where SRC the judges will evaluate the participants based on the two videos (short pitch and the longer presentation video).<br/>
Entries are judged based on: (i) Motivation of the research, (ii) Background work, (iii) Approach, (iv) Results and contributions to the field of mobile systems.
<br/><br/>
Selected students will advance to the second round, where they will give a 10-minute presentation on their research, followed by a 5-minute question and answer period. This will be organized also as a virtual session, after the Demo/Poster sessions.
<br/><br/>
<h5><b>Awards</b></h5>
The first, second, and third place winners from each category will be awarded $500, $300, and $200 respectively. The first-place graduate and undergraduate winners will advance to the SRC Grand Finals, where winners from various ACM SIGs are evaluated to nominate the SRC winners. The winners of the Grand Finals will be recognized at the Annual ACM Awards Banquet, the same banquet that also recognizes the Turing award winners.
<br/><br/>
<h5><b>Submission</b></h5>
Students submitting to the MobiCom 2020 Poster/Demo Session can opt to have their work considered by the SRC committee, if their submissions are accepted. To be considered for the SRC, complete a submission to the Poster or Demo Session and indicate in the submission form that you would like to participate in the SRC. Your submission should describe recently completed or ongoing research. If part of a group project, clearly state your contributions in the submitted abstract and poster. For Submission Deadline and Instructions, please follow the guidelines on the MobiCom 2020 Posters or MobiCom 2020 Demo pages.
