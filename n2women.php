<b>Fellowship Overview and Application Instructions</b><br/>
<p>N2Women is pleased to announce the continuation of our N2Women Young Researcher Fellowship awards. N2Women considers you a young researcher if you are currently a student, working as a post-doc, or in the 1st or 2nd year of your new research career. These awards will partially cover a young researcher’s travel cost (up to $1000) to a meeting where an N2Women event will be held.</p>

<p>In exchange, the young researcher must help organize the N2Women meeting and attend the event. The MobiCom'19 N2Women event will be a dinner, which will be held in the evening of October 22. The benefit of doing the organization, in addition to the travel funds, is for the young researcher to connect with the organizers of the conference who are, typically, leaders in the research field. We will arrange for a faculty or research member of N2Women to assist/mentor the young researcher in this task.</p>

<p>Support for these N2Women Young Researcher Fellowship awards has been generously provided by ACM SIGMOBILE.</p>

